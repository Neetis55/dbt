WITH
CIPO_POS_REPORTING_MATRIX AS
(
SELECT 
    DIRECT_CORPORATION_CODE,
    COALESCE(TRY_TO_BOOLEAN(POS),'FALSE')::BOOLEAN AS IS_POS_FLAG,
    COALESCE(TRY_TO_BOOLEAN(BILL),'FALSE')::BOOLEAN AS IS_BILL_FLAG,
    FROM_DATE,
    TO_DATE
 FROM {{source('STG_SHAREPOINT_MDL','CIPO_POS_REPORTING_MATRIX')}} --- need to find pk
QUALIFY(ROW_NUMBER() OVER (PARTITION BY DIRECT_CORPORATION_CODE,FROM_DATE ORDER BY BIW_UPD_DTTM DESC) =1)
),

--------------------------------- BILLING --------------------------------------------
MART_BILLING AS
(
    SELECT
    CASE WHEN
    TRY_TO_NUMBER(BILL.BILLING_KEY) IS NOT NULL
    THEN MD5(BILL.BILLING_KEY)
    ELSE BILL.BILLING_KEY
    END AS TRANSACTION_KEY,
    BILL.BILLING_DATE_KEY,
    BILL.DIRECT_CUSTOMER_KEY,
    BILL.INDIRECT_CUSTOMER_KEY,
    BILL.END_CUSTOMER_KEY,
    MD5(COALESCE(CUST.CORPORATION_CODE,'-1')::STRING) AS DIRECT_CORPORATION_KEY,
    MD5('-1') AS INDIRECT_CORPORATION_KEY,
    BILL.END_CORPORATION_KEY,
    BILL.MARKET_PRODUCT_NUMBER_KEY,
    BILL.INTERNAL_PART_NUMBER_KEY,
    (BILL.BILLING_KEY * -1)::NUMBER AS TRANSACTION_ID,
    BILL.PROCESS_DATE,
    BILL.DIRECT_CUSTOMER_CODE,
    BILL.INDIRECT_CUSTOMER_CODE,
    BILL.END_CUSTOMER_CODE,
    CUST.CORPORATION_CODE AS DIRECT_CORPORATION_CODE,
    '-1' AS INDIRECT_CORPORATION_CODE,
    BILL.END_CORPORATION_CODE,
    BILL.MARKET_PRODUCT_NUMBER,
    BILL.INTERNAL_PART_NUMBER,
    NULL AS TRANSACTION_CODE,
    NULL AS REPORTED_SHIP_DATE,
    NULL AS REPORTED_BRANCH_ID,
    BILL.BILLING_NET_QUANTITY,
    NULL AS DISTRIBUTOR_SHIP_TO_CUSTOMER_IDENTIFIER,
    NULL AS DISTRIBUTOR_SHIP_TO_CUSTOMER_NAME,
    BILL.DIST_END_CUSTOMER_CODE,
    NULL AS DISTRIBUTOR_END_CUSTOMER_NAME,
    NULL AS DISTRIBUTOR_INVOICE_NUMBER,
    NULL AS DISTRIBUTOR_INVOICE_LINE_NUMBER,
    NULL AS REGISTRATION_NUMBER,
    NULL AS DEBIT_NUMBER,
    BILL.CUSTOMER_PO_NUMBER,
    BILL.TRANSACTION_CURRENCY_ISO_CODE,
    0 AS RESALE_UNIT_PRICE,
    0 AS RESALE_UNIT_PRICE_USD,
    BILL.BILLING_NET_AMOUNT_TRANSACTION_CURRENCY AS RESALE_NET_AMOUNT,
    BILL.BILLING_NET_AMOUNT_USD AS RESALE_NET_AMOUNT_USD,
    BILL.BILLING_NET_AMOUNT_EUR AS RESALE_NET_AMOUNT_EUR,
    BILL.BILLING_NET_AMOUNT_JPY AS RESALE_NET_AMOUNT_JPY,
    0 AS DISTRIBUTOR_UNIT_PRICE,
    0 AS DISTRIBUTOR_UNIT_PRICE_USD,
    0 AS DISTRIBUTOR_COST_AMOUNT,
    BILL.BILLING_NET_AMOUNT_USD AS DISTRIBUTOR_COST_AMOUNT_USD,
    BILL.BILLING_NET_AMOUNT_EUR AS DISTRIBUTOR_COST_AMOUNT_EUR,
    BILL.BILLING_NET_AMOUNT_JPY AS DISTRIBUTOR_COST_AMOUNT_JPY,
    'BILLING' AS SOURCE_DATA_FROM,
    NULL AS ONSALES_CREATE_DATE,
    NULL AS ONSALES_MODIFIED_DATE,
    1::BOOLEAN AS IS_TRUE_POS,
    1::BOOLEAN AS IS_SDM_TRUE_POS,
    0::BOOLEAN AS IS_VAR,
    NULL AS END_CORPORATION_DECODE,
    BILL.IS_TRUE_BILL,
    BILL.BIW_INS_DTTM,
    BILL.BIW_UPD_DTTM,
    BILL.BIW_BATCH_ID,
    'N' AS BIW_LOGICAL_DELETE_FLAG
FROM
    {{ ref('MART_SALES_BILLING_FACT')}} BILL
        LEFT JOIN {{ref('SALES_PRODUCT')}} PRDCT 
    ON BILL.INTERNAL_PART_NUMBER_KEY=PRDCT.PART_KEY
        LEFT JOIN {{ref('MART_SALES_CUSTOMER')}} CUST 
    ON BILL.DIRECT_CUSTOMER_CODE=CUST.CUSTOMER_CODE 
WHERE  
  CASE 
    WHEN  SOURCE_OF_SALE='ON' AND LEFT(PRDCT.PAL2_CODE,1) in ('A','B','C','D','F','G','H','I','J','K','L','M','N','O','P','Q','R','S','T','V','W','X','Y')
      THEN 1 
    WHEN SOURCE_OF_SALE<>'ON'
      THEN 1
  END =1
    AND BILL.BUSINESS_CLASS_CODE = 'CIPO' AND BILL.PROCESS_DATE>='2016-01-01' AND BILL.IS_TRUE_BILL ='TRUE'
    AND (EXISTS (select * from
    CIPO_POS_REPORTING_MATRIX CIPO where CUST.CORPORATION_CODE=CIPO.DIRECT_CORPORATION_CODE
	AND BILL.PROCESS_DATE BETWEEN CIPO.FROM_DATE AND CIPO.TO_DATE 
	AND  CIPO.IS_BILL_FLAG='Yes')
	OR
	CUST.CORPORATION_CODE NOT IN (Select DIRECT_CORPORATION_CODE from CIPO_POS_REPORTING_MATRIX))
)

